package string;

public class Hero {

		String name;
		double hp;
		float heroArmor;
		int heroMoveSpeed;
		public Hero()
		{
			
		}
		public Hero(String heroName,double heroHP,float heroArmor,int heroMoveSpeed)
		{
			name=heroName;
			hp=heroHP;
			this.heroArmor=heroArmor;
			this.heroMoveSpeed=heroMoveSpeed;
			
		}
}
